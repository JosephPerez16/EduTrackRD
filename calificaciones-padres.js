function getStatusClass(estado) {
  if (estado === "Excelente") return "status-excellent";
  if (estado === "Satisfactorio") return "status-good";
  return "status-warning";
}

function getEstado(promedio) {
  if (promedio >= 90) return "Excelente";
  if (promedio >= 80) return "Satisfactorio";
  return "En seguimiento";
}

function getGradeClass(promedio) {
  if (promedio >= 90) return "grade-high";
  if (promedio >= 80) return "grade-mid";
  return "grade-low";
}

function getPerformanceClass(estado) {
  if (estado === "Excelente") return "performance-excellent";
  if (estado === "Satisfactorio") return "performance-good";
  return "performance-warning";
}

function cargarEstudiante() {
  const data = localStorage.getItem("hijoSeleccionado");

  if (!data) return null;

  try {
    return JSON.parse(data);
  } catch (error) {
    return null;
  }
}

function renderStudentInfo(estudiante) {
  document.getElementById("studentName").textContent = estudiante.nombre || "-";
  document.getElementById("studentId").textContent = estudiante.id || "-";
  document.getElementById("studentGrade").textContent = estudiante.grado || "-";
  document.getElementById("studentSection").textContent = estudiante.seccion || "-";
  document.getElementById("studentYear").textContent = estudiante.anoEscolar || "-";
  document.getElementById("studentSchool").textContent = estudiante.centro || "-";
}

function obtenerCuatrimestreSeleccionado() {
  return document.getElementById("quarterSelect").value;
}

function obtenerCalificaciones(estudianteId, cuatrimestre) {
  const estudianteData = calificacionesPorEstudiante[estudianteId];
  if (!estudianteData) return [];
  return estudianteData[cuatrimestre] || [];
}

function renderResumen(calificacionesConPromedio) {
  const averageGrade = document.getElementById("averageGrade");
  const approvedSubjects = document.getElementById("approvedSubjects");
  const totalSubjects = document.getElementById("totalSubjects");

  if (calificacionesConPromedio.length === 0) {
    averageGrade.innerHTML = `<div class="performance-wrap"><span class="summary-highlight grade-low">0</span><span class="performance-label performance-warning">Sin datos</span></div>`;
    approvedSubjects.textContent = "0/0";
    totalSubjects.textContent = "0";
    return;
  }

  const total = calificacionesConPromedio.length;
  const aprobadas = calificacionesConPromedio.filter(item => item.promedio >= 70).length;
  const promedioGeneral = Math.round(
    calificacionesConPromedio.reduce((acc, item) => acc + item.promedio, 0) / total
  );
  const estadoGeneral = getEstado(promedioGeneral);

  averageGrade.innerHTML = `
    <div class="performance-wrap">
      <span class="summary-highlight ${getGradeClass(promedioGeneral)}">${promedioGeneral}</span>
      <span class="performance-label ${getPerformanceClass(estadoGeneral)}">${estadoGeneral}</span>
    </div>
  `;

  approvedSubjects.textContent = `${aprobadas}/${total}`;
  totalSubjects.textContent = total;
}

function renderTabla(estudiante, cuatrimestre) {
  const tbody = document.getElementById("gradesTableBody");
  const calificaciones = obtenerCalificaciones(estudiante.id, cuatrimestre);

  if (calificaciones.length === 0) {
    tbody.innerHTML = `
      <tr>
        <td colspan="8" class="empty-state">No hay calificaciones registradas para este cuatrimestre.</td>
      </tr>
    `;
    renderResumen([]);
    return;
  }

  const calificacionesConPromedio = calificaciones.map(item => {
    const promedio = Math.round((item.p1 + item.p2 + item.p3 + item.examen) / 4);
    const estado = getEstado(promedio);

    return {
      ...item,
      promedio,
      estado,
      cuatrimestre
    };
  });

  tbody.innerHTML = calificacionesConPromedio.map(item => `
    <tr>
      <td class="subject-name">${item.materia}</td>
      <td class="grade-value">${item.p1}</td>
      <td class="grade-value">${item.p2}</td>
      <td class="grade-value">${item.p3}</td>
      <td class="grade-value">${item.examen}</td>
      <td>
        <span class="final-grade ${getGradeClass(item.promedio)}">${item.promedio}</span>
      </td>
      <td><span class="status-badge ${getStatusClass(item.estado)}">${item.estado}</span></td>
      <td>${item.cuatrimestre}</td>
    </tr>
  `).join("");

  renderResumen(calificacionesConPromedio);
}

function initPage() {
  const estudiante = cargarEstudiante();
  const quarterSelect = document.getElementById("quarterSelect");

  if (!estudiante) {
    window.location.href = "mis-hijos.html";
    return;
  }

  renderStudentInfo(estudiante);
  renderTabla(estudiante, obtenerCuatrimestreSeleccionado());

  quarterSelect.addEventListener("change", () => {
    renderTabla(estudiante, obtenerCuatrimestreSeleccionado());
  });
}

initPage();