function cargarEstudiante() {
  const data = localStorage.getItem("hijoSeleccionado");

  if (!data) return null;

  try {
    return JSON.parse(data);
  } catch (error) {
    return null;
  }
}

function renderStudentInfo(estudiante) {
  document.getElementById("studentName").textContent = estudiante.nombre || "-";
  document.getElementById("studentId").textContent = estudiante.id || "-";
  document.getElementById("studentGrade").textContent = estudiante.grado || "-";
  document.getElementById("studentSection").textContent = estudiante.seccion || "-";
  document.getElementById("studentYear").textContent = estudiante.anoEscolar || "-";
  document.getElementById("studentSchool").textContent = estudiante.centro || "-";
}

function getBadgeClass(estado) {
  if (estado === "Excelente") return "status-high";
  if (estado === "Satisfactorio") return "status-mid";
  return "status-low";
}

function getResumenClass(valor) {
  const texto = valor.toLowerCase();

  if (texto.includes("sobresaliente") || texto.includes("excelente") || texto.includes("muy positivo") || texto.includes("destacada") || texto.includes("muy activa")) {
    return "status-high";
  }

  if (texto.includes("positivo") || texto.includes("muy buena") || texto.includes("constante") || texto.includes("activa") || texto.includes("buena")) {
    return "status-mid";
  }

  return "status-low";
}

function obtenerCuatrimestreSeleccionado() {
  return document.getElementById("quarterSelect").value;
}

function obtenerObservaciones(estudianteId, cuatrimestre) {
  const estudianteData = observacionesPorEstudiante[estudianteId];
  if (!estudianteData) return null;
  return estudianteData[cuatrimestre] || null;
}

function renderResumen(data) {
  const generalStatus = document.getElementById("generalStatus");
  const conductStatus = document.getElementById("conductStatus");
  const participationStatus = document.getElementById("participationStatus");

  if (!data) {
    generalStatus.innerHTML = `<span class="status-highlight status-low">Sin datos</span>`;
    conductStatus.textContent = "-";
    participationStatus.textContent = "-";
    return;
  }

  generalStatus.innerHTML = `<span class="status-highlight ${getResumenClass(data.resumen.general)}">${data.resumen.general}</span>`;
  conductStatus.textContent = data.resumen.conducta;
  participationStatus.textContent = data.resumen.participacion;
}

function renderObservaciones(estudiante, cuatrimestre) {
  const observationsGrid = document.getElementById("observationsGrid");
  const data = obtenerObservaciones(estudiante.id, cuatrimestre);

  if (!data || !data.items || data.items.length === 0) {
    observationsGrid.innerHTML = `<div class="empty-state">No hay observaciones registradas para este cuatrimestre.</div>`;
    renderResumen(null);
    return;
  }

  observationsGrid.innerHTML = data.items.map(item => `
    <article class="observation-card">
      <div class="observation-top">
        <div class="observation-category">
          <div class="observation-icon">
            <i class="${item.icono}"></i>
          </div>
          <div>
            <h3>${item.categoria}</h3>
            <span>${item.subtitulo}</span>
          </div>
        </div>
        <span class="observation-badge ${getBadgeClass(item.estado)}">${item.estado}</span>
      </div>
      <div class="observation-body">
        <p>${item.texto}</p>
      </div>
    </article>
  `).join("");

  renderResumen(data);
}

function initPage() {
  const estudiante = cargarEstudiante();
  const quarterSelect = document.getElementById("quarterSelect");

  if (!estudiante) {
    window.location.href = "mis-hijos.html";
    return;
  }

  renderStudentInfo(estudiante);
  renderObservaciones(estudiante, obtenerCuatrimestreSeleccionado());

  quarterSelect.addEventListener("change", () => {
    renderObservaciones(estudiante, obtenerCuatrimestreSeleccionado());
  });
}

initPage();